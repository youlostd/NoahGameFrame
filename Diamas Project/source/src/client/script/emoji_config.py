def ReplaceEmoticons(text, isShout=False):
    emoticonTag = "|E{0}|e"
    for k, v in EMOJI_DICT.iteritems():
        if isShout and v["disable_for_players"]:
            continue
        for code in v["codes"]:
            text = text.replace(code, emoticonTag.format(k))

    return text


def RegisterEmoticon():
    for k, v in EMOJI_DICT.iteritems():
        emoji.instance().Register(k, v["file"])
            

